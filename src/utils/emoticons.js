// Get all emoticons for the picker (removes duplicates)
export const getAllEmoticons = () => {
  return Object.entries(emoticons)
    .filter(([, value], index, arr) => {
      // Show only unique emoticons (filter duplicates)
      const firstIndex = arr.findIndex(([, v]) => v.gif === value.gif);
      return arr.indexOf(arr[firstIndex]) === index;
    })
    .map(([shortcut, data]) => ({ shortcut, ...data }));
};
